"""Convert public solar-defect detection datasets to Ultralytics YOLO layout.

The converter reuses the shared loaders in ``data_tools.utils`` and writes only
standard YOLO text labels plus symlinks to existing local images.  It does not
implement detection logic; Ultralytics consumes the output dataset for training,
validation, and export workflows.
"""

from __future__ import annotations

import argparse
import random
import shutil
import sys
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[2]
if str(PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(PROJECT_ROOT))

from data_tools.utils import PROJECT_ROOT as ROOT
from data_tools.utils import VocImageRecord, read_pv_multi_defect, read_pvel_ad


PVEL_AD_CLASSES = [
    "finger",
    "crack",
    "black_core",
    "thick_line",
    "horizontal_dislocation",
    "short_circuit",
    "vertical_dislocation",
    "star_crack",
    "printing_error",
    "corner",
    "fragment",
    "scratch",
]

PV_MULTI_DEFECT_CLASSES = [
    "black_border",
    "broken",
    "hot_spot",
    "no_electricity",
    "scratch",
]


def yolo_box(record: VocImageRecord, obj_name: str, xmin: int, ymin: int, xmax: int, ymax: int, class_to_id: dict[str, int]) -> str:
    """Convert one absolute Pascal VOC box to one normalized YOLO label row."""

    x_center = ((xmin + xmax) / 2) / record.width
    y_center = ((ymin + ymax) / 2) / record.height
    width = (xmax - xmin) / record.width
    height = (ymax - ymin) / record.height
    return f"{class_to_id[obj_name]} {x_center:.6f} {y_center:.6f} {width:.6f} {height:.6f}"


def link_or_copy_image(source: Path, destination: Path) -> None:
    """Place an image in the YOLO image tree using a symlink when possible."""

    destination.parent.mkdir(parents=True, exist_ok=True)
    if destination.exists() or destination.is_symlink():
        destination.unlink()
    try:
        destination.symlink_to(source)
    except OSError:
        shutil.copy2(source, destination)


def write_record(record: VocImageRecord, split: str, output_root: Path, class_to_id: dict[str, int]) -> None:
    """Write one image link and its YOLO label file for a dataset split."""

    image_out = output_root / "images" / split / record.image_path.name
    label_out = output_root / "labels" / split / f"{record.image_path.stem}.txt"
    link_or_copy_image(record.image_path, image_out)
    label_out.parent.mkdir(parents=True, exist_ok=True)

    rows: list[str] = []
    for obj in record.objects:
        if obj.name not in class_to_id:
            raise ValueError(f"Unknown class '{obj.name}' in {record.xml_path}")
        rows.append(
            yolo_box(
                record=record,
                obj_name=obj.name,
                xmin=obj.xmin,
                ymin=obj.ymin,
                xmax=obj.xmax,
                ymax=obj.ymax,
                class_to_id=class_to_id,
            )
        )
    label_out.write_text("\n".join(rows) + ("\n" if rows else ""), encoding="utf-8")


def write_dataset_yaml(output_root: Path, class_names: list[str], train_split: str, val_split: str) -> None:
    """Write the Ultralytics dataset YAML file consumed by YOLO commands."""

    names = "\n".join(f"  {index}: {name}" for index, name in enumerate(class_names))
    content = f"""# Ultralytics YOLO dataset config generated by data_tools/converters.
path: {output_root.relative_to(ROOT).as_posix()}
train: images/{train_split}
val: images/{val_split}

names:
{names}
"""
    (output_root / "dataset.yaml").write_text(content, encoding="utf-8")


def build_pvel_ad(output_root: Path) -> None:
    """Build a YOLO dataset from PVEL-AD trainval and released test annotations."""

    dataset = read_pvel_ad()
    class_to_id = {name: index for index, name in enumerate(PVEL_AD_CLASSES)}
    for split, records in dataset["records"].items():
        for record in records:
            write_record(record, split, output_root, class_to_id)
    write_dataset_yaml(output_root, PVEL_AD_CLASSES, train_split="trainval", val_split="test")


def split_records(records: list[VocImageRecord], val_ratio: float, seed: int) -> tuple[list[VocImageRecord], list[VocImageRecord]]:
    """Create a deterministic train and validation split for unsplit datasets."""

    shuffled = list(records)
    random.Random(seed).shuffle(shuffled)
    val_count = max(1, int(len(shuffled) * val_ratio))
    return shuffled[val_count:], shuffled[:val_count]


def build_pv_multi_defect(output_root: Path, val_ratio: float, seed: int) -> None:
    """Build a YOLO dataset from PV-Multi-Defect VOC annotations."""

    dataset = read_pv_multi_defect()
    train_records, val_records = split_records(dataset["records"], val_ratio, seed)
    class_to_id = {name: index for index, name in enumerate(PV_MULTI_DEFECT_CLASSES)}
    for record in train_records:
        write_record(record, "train", output_root, class_to_id)
    for record in val_records:
        write_record(record, "val", output_root, class_to_id)
    write_dataset_yaml(output_root, PV_MULTI_DEFECT_CLASSES, train_split="train", val_split="val")


def parse_args() -> argparse.Namespace:
    """Parse converter command-line arguments."""

    parser = argparse.ArgumentParser(description="Build Ultralytics YOLO datasets from local VOC annotations.")
    parser.add_argument("--dataset", choices=["pvel_ad", "pv_multi_defect"], required=True)
    parser.add_argument("--output-root", type=Path, default=ROOT / "datasets" / "processed" / "yolo")
    parser.add_argument("--val-ratio", type=float, default=0.2)
    parser.add_argument("--seed", type=int, default=42)
    return parser.parse_args()


def main() -> None:
    """Convert the selected dataset to a YOLO-compatible processed directory."""

    args = parse_args()
    output_root = args.output_root / args.dataset
    output_root.mkdir(parents=True, exist_ok=True)
    if args.dataset == "pvel_ad":
        build_pvel_ad(output_root)
    else:
        build_pv_multi_defect(output_root, val_ratio=args.val_ratio, seed=args.seed)
    print(f"Wrote YOLO dataset to {output_root}")


if __name__ == "__main__":
    main()
